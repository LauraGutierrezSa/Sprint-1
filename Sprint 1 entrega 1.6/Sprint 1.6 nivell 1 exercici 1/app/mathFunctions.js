function sum (num1, num2) {  
    const result = num1+num2;
    return result
}

function substract (num1, num2) {
    const result = num1-num2;
    return result
}

function multiply (num1, num2) {
    const result = num1*num2;
    return result
}

function divide (num1, num2) {
    const result = num1/num2;
    return result
}

module.exports = {
    sum,
    substract,
    multiply,
    divide
}
